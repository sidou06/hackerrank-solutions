def climbingLeaderboard(ranked, player):
    # Write your code here
    res = []
    rank = 1
    i = 0
    for score in reversed(player):
        while i < len(ranked) and ranked[i] > score:
            if i == 0:
                rank += 1
            elif ranked[i] != ranked[i - 1]:
                rank += 1
            i = i + 1
        res.append(rank)
    return list(reversed(res))

